// Operadores Aritméticos
// Os operadores aritméticos são usados para realizar cálculos matemáticos 
// básicos em programação. Eles operam sobre valores e variáveis, permitindo 
// que você execute operações como adição, subtração, multiplicação, divisão e 
// cálculo de resto. Aqui estão os principais operadores aritméticos ( em PHP e Javascript ):

// Adição (+): Soma dois valores. Exemplo: a + b.
// Subtração (-): Subtrai o segundo valor do primeiro. Exemplo: a - b.
// Multiplicação (*): Multiplica dois valores. Exemplo: a * b.
// Divisão (/): Divide o primeiro valor pelo segundo. Exemplo: a / b.
// Módulo (%): Retorna o resto da divisão do primeiro valor pelo segundo. Exemplo: a % b.
// Exponenciação (**): Eleva o primeiro número à potência do segundo. Exemplo: a ** b.

// Esses operadores são essenciais para muitas operações matemáticas e algoritmos em programação. Compreender como usá-los é fundamental para resolver problemas e criar soluções eficientes.

// Exemplos de operadores aritméticos em ação:

$num1 = 10;
$num2 = 5;

// Soma
$soma = $num1 + $num2;
echo "A soma de $num1 e $num2 é: " . $soma . "\n";

// Subtração
$subtracao = $num1 - $num2;
echo "A subtração de $num1 e $num2 é: " . $subtracao . "\n";

// Multiplicação
$multiplicacao = $num1 * $num2;
echo "A multiplicação de $num1 por $num2 é: " . $multiplicacao . "\n";

// Divisão
$divisao = $num1 / $num2;
echo "A divisão de $num1 por $num2 é: " . $divisao . "\n";

// Módulo (resto da divisão)
$modulo = $num1 % $num2;
echo "O módulo de $num1 e $num2 é: " . $modulo . "\n";

// Exponenciação
$exponenciacao = $num1 ** $num2;
echo "$num1 elevado a $num2 é: " . $exponenciacao . "\n";

